            <p>Banyuwangi – RSUD Blambangan Kabupaten Banyuwangi, segera memiliki pusat jantung terpadu, Banyuwangi Cardiac Center (BCC). Ini setelah RSUD Blambangan ditetapkan oleh Kementerian Kesehatan RI sebagai rumas sakit jejaring rujukan nasional untuk penyakit kardiovaskular (jantung).</p>
            
            <p>“Ini menjadi pemicu kami untuk terus meningkatkan kualitas layanan kesehatan di fasilitas kesehatan milik pemerintah. Pemkab akan terus mendukung peningkatan sarana dan prasarana yang dibutuhkan untuk keperluan layanan penyakit jantung tersebut,” kata Bupati Banyuwangi, Ipuk Fiestiandani, Senin (20/3/2023).</p>
             
            <p>Dengan hadirnya BCC, kata Ipuk, penderita penyakit kardiovaskular Banyuwangi tidak perlu lagi dirujuk ke luar daerah. “Bahkan coverage kita akan bertambah karena menjadi rujukan bagi wilayah sekitar. Seperti Bondowoso, Situbondo, hingga Jembrana Bali,” ujarnya.
              
            <p>Direktur RSUD Blambangan dr Widji Lestariono menambahkan, sebagai RS jejaring rujukan nasional segala peralatan dan kelengkapan tindakan kateterisasi harus lengkap. Saat ini, RSUD Blambangan mampu melakukan kateterisasi jantung atau pemasangan ring jantung sekitar 30 tindakan dalam sebulan.</p>

              <p>“Dengan penetapan tersebut, maka kapasitas layanan jantung di RSUD Blambangan akan bertambah. Sehingga diperlukan sarana prasarana, peralatan, hingga SDM yang lebih memadahi. Untuk itu, kami akan membangun gedung BCC,” ungkap Rio, sapaan akrab dr. Widji Lestariono.</p>

                <p>Meski demikian, kata Rio, saat ini RSUD Blambangan telah memiliki berbagai peralatan medis pendukung yang cukup canggih. Termasuk layanan Cath Lab (Kateterisasi Jantung) atau pemasangan ring jantung yang belum bisa dilakukan oleh semua rumah sakit.</p>
           
