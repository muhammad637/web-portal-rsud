            <h2 class="mb-3 mt-5">Anak-Anak Rentan Flu dan Batuk Akibat Kualitas Udara Buruk
            </h2>
            <p>Dijelaskan Carlinda, banyaknya kendaraan bermotor, perilaku bakar sampah, dan menurunnya intensitas curah hujan menjadi faktor utama penyebab kualitas udara di Jakarta dan daerah sekitar di Indonesia yang semakin memburuk setiap harinya. Carlinda, menekankan, kondisi kualitas udara yang buruk berpengaruh pada kesehatan pernapasan. Jika melihat berdasarkan jenjang usia, anak-anak adalah kelompok usia yang paling rentan terkena flu dan batuk.
              "Ini semua terjadi karena kualitas udara yang buruk," ujarnya.Meski begitu, Carlinda mengatakan bahwa flu dan batuk adalah penyakit yang umum terjadi, sehingga orang tua tidak perlu
              khawatir. Lebih lanjut Carlinda memberikan beberapa tips yang dapat membantu orang tua mengatasi batuk dan flu berkepanjangan pada anak :</p>
              <h2 class="mb-3">1. Berikan Si Kecil Air Minum Yang Cukup
              </h2>
            <p>Guna membantu perawatan anak saat flu dan batuk, Carlinda menyarankan para ibu untuk memastikan cairan Si Kecil terpenuhi dengan rutin memberikan air putih yang cukup. "Kecukupan cairan membantu anak mencegah dari dehidrasi," katanya. Dijelaskannya, ketika anak masih berumur di bawah 6 bulan, ibu dapat memberikan air susu ibu (ASI) yang cukup.
              Cara ini dapat membantu anak mengeluarkan lendir yang mengganggu di tubuhnya.</p>
              <h2 class="mb-3">2. Pastikan Anak Cukup Beristirahat
              </h2>
            <p>Anak yang terkena flu dan batuk terkadang menjadi lebih uring-uringan dibandingkan biasanya. Tidak jarang juga anak mengalami kesulitan untuk tidur.
              Dalam kondisi ini, kata Carlinda, ibu dapat menemani buah hati tercinta dan memastikan mereka dapat tidur yang cukup dan nyenyak.
              Menurut Carlinda, dengan tidur yang cukup dapat membantu mempercepat penyembuhan anak.
              Selain itu, ada baiknya mengurangi aktivitas anak bermain di luar rumah untuk sementara hingga gejala flu dan batuk yang dialami membaik.</p>
              <h2 class="mb-3">3. Jauhkan Anak dari Makanan dan Minuman Penyebab Batuk
              </h2>
              <p>Saat anak batuk, orang tua harus memastikan anaknya tidak menyantap makanan dan minuman yang dapat menyebabkan batuk, seperti minuman manis, minuman dingin, serta makanan yang pedas dan gorengan.
                Saat kondisi Si Kecil dalam keadaan batuk atau flu, Carlinda menyarankan para ibu memberikan anak mereka makanan berkuah yang hangat.
                "Ini dapat membantu meringankan rasa gatal di tenggorokan serta mengatasi hidung tersumbat," ujarnya.</p>
                <h2 class="mb-3">4. Berikan Obat Batuk Khusus Anak
                </h2>
                <p>Selain itu, kata Carlinda, ibu dapat memberikan obat batuk khusus anak. Cara ini efektif membantu mengatasi batuk mereka.
                  Namun, Carlinda, mengingatkan, sebelum memberikan obat batuk, ada baiknya untuk berkonsultasi terlebih dahulu kepada dokter terkait penyakit Si Kecil.
                  Pemberian obat batuk untuk si kecil harus sesuai dengan dosis yang dianjurkan dan melihat aturan pakai yang tertera pada label kemasan.</p>
           
